// ----------------------------------------------------------------------------------------------------------------------
// Behavior that destroys the attached particle system.
// ----------------------------------------------------------------------------------------------------------------------

using UnityEngine;

// ----------------------------------------------------------------------------------------------------------------------

public class PSDestroy : MonoBehaviour
{
    // Use this for initialization
    private void Start()
    {
        Destroy(this.gameObject, this.GetComponent<ParticleSystem>().main.duration);
    } // end Start
} // end PSDestroy

